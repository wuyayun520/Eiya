- (void) hideFusedGrainType: (int)graphWithVisitor
{
	dispatch_async(dispatch_get_main_queue(), ^{
		int decorationThroughActivity[graphWithVisitor];
		for (int i = 0; i < graphWithVisitor; i++) {
			decorationThroughActivity[i] = i * 1;
		}
		int monsterAsCycle = (int)(sizeof(decorationThroughActivity) / sizeof(int));
		for (int i = 0; i < monsterAsCycle/2; i++) {
			decorationThroughActivity[monsterAsCycle - i - 1] = 10;
		}
		//NSLog(@"Business17 gen_int executed%@", Business17);
	});
}


